// check if there exists any item that represents a supported region
export const existRegionItems = props => {
  const selectedData = props.data.filter((item, i) =>
    props.rowSelections.includes(i)
  )
  let existed = false

  selectedData.forEach(item => {
    if (
      Object.values(map2Settings[props.moreSettings.map2].names).includes(
        item[props.header[props.settings.region]]
      )
    ) {
      existed = true
      return
    }
  })

  return existed
}
